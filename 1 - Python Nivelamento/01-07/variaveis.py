#variáveis: Definição e tipos

#declarando 4 variáveis e atribuindo valores a elas

var_string = 'hello!'
var_number = 9
var_float = 9.0
var_bool_t = True

#imprimir valor das variáveis
print(var_string)
print(var_number)
print(var_float)
print(var_bool_t)

print('---------------')

#imprimir tipos das variáveis
print(type(var_string))
print(type(var_number))
print(type(var_float))
print(type(var_bool_t))

print('---------------')

#conversão de variáveis
print(var_float)
print(int(var_float))
print(str(var_float))